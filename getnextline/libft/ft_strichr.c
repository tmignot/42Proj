int		ft_strichr(char *str, int l)
{
	char	c;
	int		i;

	i = 0;
	c = (char)l;
	while (str[i] != c)
	{
		if (str[i] == '\0' && c != '\0')
			return (-1);
		i++;
	}
	return (i);
}
