#include "libft.h"

int		ft_lstcount(t_list *lst)
{
	int		i;

	i = 0;
	if (lst != NULL)
	{
		i = 1;
		while (lst->next)
		{
			lst = lst->next;
			i++;
		}
	}
	return (i);
}
